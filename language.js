// Хэлний удирдагчийн анги
class LanguageManager {
    constructor() {
        this.currentLanguage = localStorage.getItem('language') || 'en';
        this.translations = translations;
    }

    // Одоогийн хэлийг авах
    getCurrentLanguage() {
        return this.currentLanguage;
    }

    // Хэл тохируулах
    setLanguage(lang) {
        if (this.translations[lang]) {
            this.currentLanguage = lang;
            localStorage.setItem('language', lang);
            this.updatePageLanguage();
            return true;
        }
        return false;
    }

    // Түлхүүрийн орчуулгыг авах
    translate(key) {
        return this.translations[this.currentLanguage]?.[key] || key;
    }

    // Хуудас дээрх бүх элементүүдийг орчуулгаар шинэчлэх
    updatePageLanguage() {
        // HTML lang шинж чанарыг шинэчлэх
        document.documentElement.lang = this.currentLanguage;

        // data-i18n шинж чанар бүхий бүх элементүүдийг шинэчлэх
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.translate(key);
            if (translation) {
                element.textContent = translation;
            }
        });

        // Placeholder-уудыг шинэчлэх
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            const translation = this.translate(key);
            if (translation) {
                element.placeholder = translation;
            }
        });
        
        // Хэлээс хамаарах динамик агуулгыг дахин харуулах
        if (typeof showTaskListPage === 'function') {
            const currentPage = typeof appState !== 'undefined' ? appState.getStateProperty('currentPage') : 'dashboard';
            if (currentPage === 'taskList') {
                showTaskListPage();
            }
        }
        
        if (typeof renderTasks === 'function') {
            const tasks = typeof appState !== 'undefined' ? appState.getStateProperty('tasks') : [];
            renderTasks(tasks);
        }
    }

    // Initialize language on page load
    init() {
        this.updatePageLanguage();
    }
}

// Дэлхийн хэлний удирдагчийн жишээ үүсгэх
const languageManager = new LanguageManager();

// Бусад файлуудад ашиглахын тулд экспорт хийх
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { translations, LanguageManager, languageManager };
}

